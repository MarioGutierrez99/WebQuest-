1. ¿Qué es un control de versiones?

Se llama control de versiones a la gestión de los diversos cambios que se realizan 
sobre los elementos de algún producto o una configuración del mismo. Una versión, 
revisión o edición de un producto, es el estado en el que se 
encuentra el mismo en un momento dado de su desarrollo o modificación.

2. ¿Cuáles son las ventajas de utilizar un controlador de versiones?

- Acceso al registro de modificaciones sobre un fichero. 
Esto nos permite además ver los comentarios asociados a cada 
modificación y la persona que ha realizado dicha modificación
- Posibilidad de regresar al estado anterior de un fichero o conjunto de ficheros
- Creación de ramas para gestionar cambios que finalmente se mezclarán con la rama principal
- Facilidad para etiquetar modificaciones concretas. Esto además, acaba desembocando 
en un DML de ITIL, con lo que conseguimos un repositorio con todas las releases del producto

3. ¿Cuáles son los controladores de versiones disponibles para android studio?

-GitLab
