-BitBucket
-Azure DevOps

4. ¿Cuáles son los servidores de controladores de versiones mas conocidos?

    -Git 
    -CVS
    -Mercurial
    -Subversion
    -Bazaar

5. ¿Investigue los siguientes terminos Push, Commit, Branch, Delete, Stack y Repositorio?

    -Push: Es un comando que sube cambios hechos en tu ambito de trabajo a una rama de trabajo
    de mi ordenanador y/o de tu equipo remota.

    -Commit: Es la acción de guardar o subir tus archivos a tu repositorio remoto (es una 
    actualización de tus cambios) también puede hacerse al local (depende donde hayas creado 
    tu repositorio).

    -Branch: Es cuando se van a llevar a cabo cambios importantes que romperán 
    la compilación, pruebas, experimentos o intentos de optimización, debe crearse 
    una nueva rama de desarrollo, con la que no molestemos a los compañeros,
    esto es un branch: una copia del código o la rama de la que deriva. .

    -Repositorio: Es el lugar en el que se almacenan los datos actualizados e históricos
    de cambios, a menudo en un servidor. A veces se le denomina depósito o depot. 
    Puede ser un sistema de archivos en un disco duro, un banco de datos, etc.. 

    -Stack: es una lista ordenada o estructura de datos que permite almacenar y 
    recuperar datos, el modo de acceso a sus elementos es de tipo LIFO 
    (del inglés Last In, First Out, «último en entrar, primero en salir»). 
    Esta estructura se aplica en multitud de supuestos en el área de informática 
    debido a su simplicidad y capacidad de dar respuesta a numerosos procesos.
